public class Arreglo {
    public void main(String[] args) {
        // se crea un arreglo de tipo string que guarde todos los nombres de los alumnos
        String[] alumnos = new String[]{"Beca", "Argel", "Leslie", "Jorge", "Rodrigo", "Mario", "Sebastian N", "Sebastian A", "Christian", "Camila", "Justin", "Carlo", "Oscar"};
        // se utiliza un for para pasar e imprimir todos los nombres del arreglo
        for (int i = 0; i < alumnos.length; i++) {
            System.out.println(alumnos[i]);
        }
    }
}